class Arithmetic_Operators{
public static void main(String []args){
int a=10;
int b=20;
int c=5;
int d=2;
System.out.println("Addition result of "+a+" and "+b+" : "+(a+b));
System.out.println("Subtraction result of "+b+" and "+a+" : "+(b-a));
System.out.println("Multiplication result of "+c+" and "+d+" : "+(c*d));
System.out.println("Division result of "+a+" and "+d+" : "+(a/d));
System.out.println("Remainder result of "+a+" and "+d+" : "+(a%d));
}
}
