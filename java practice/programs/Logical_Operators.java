class Logical_Operators{
public static void main(String []args){
boolean a=true;
boolean b=false;
System.out.println("AND result : "+(true && true));
System.out.println("AND result  : "+(true && false));
System.out.println("OR than result : "+(true || false));
System.out.println("OR than result : "+(false || false));
System.out.println("AND result of "+a+" and "+b+" : "+(a && b));
System.out.println("OR result of "+a+" and "+b+" : "+(a || b));
}
}
