class Comparision_Operators{
public static void main(String []args){
int a=10;
int b=20;
int c=5;
int d=2;
System.out.println("Equal result of "+a+" and "+b+" : "+(a==b));
System.out.println("UnEqual result of "+b+" and "+a+" : "+(b!=a));
System.out.println("Greater than result of "+c+" and "+d+" : "+(c>d));
System.out.println("Lesser than result of "+a+" and "+d+" : "+(a<d));
System.out.println("Greater than or Equal result of "+a+" and "+d+" : "+(a>=d));
System.out.println("Lesser than or Equal result of "+a+" and "+d+" : "+(a<=d));
}
}
